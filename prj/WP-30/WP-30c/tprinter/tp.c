static void tp_forward_step(int direction)
{
    PRN_POWER_DISCHARGE();
if(TP_MAX_STROBE_STEP == 2)
{
    switch (tp.phase & 0x03)
    {
        case 0:
            MOTOR_PHASE_1A_HIGH();
            MOTOR_PHASE_1B_LOW();
            MOTOR_PHASE_2A_HIGH();
            MOTOR_PHASE_2B_LOW();
            break;
        case 1:
            MOTOR_PHASE_1A_HIGH();
            MOTOR_PHASE_1B_LOW();
            MOTOR_PHASE_2A_LOW();
            MOTOR_PHASE_2B_HIGH();
            break;
        case 2:
            MOTOR_PHASE_1A_LOW();
            MOTOR_PHASE_1B_HIGH();
            MOTOR_PHASE_2A_LOW();
            MOTOR_PHASE_2B_HIGH();
            break;
        case 3:
            MOTOR_PHASE_1A_LOW();
            MOTOR_PHASE_1B_HIGH();
            MOTOR_PHASE_2A_HIGH();
            MOTOR_PHASE_2B_LOW();
            break;
    }
}else if(TP_MAX_STROBE_STEP == 4)
{
    switch (tp.phase & 0x07)
    {
    case 0:
        MOTOR_PHASE_1A_HIGH();
        MOTOR_PHASE_1B_LOW();
        MOTOR_PHASE_2A_HIGH();
        MOTOR_PHASE_2B_LOW();
        break;
    case 1:
        MOTOR_PHASE_1A_HIGH();
        MOTOR_PHASE_1B_LOW();
        MOTOR_PHASE_2A_LOW();
        MOTOR_PHASE_2B_LOW();
        break;
    case 2:
        MOTOR_PHASE_1A_HIGH();
        MOTOR_PHASE_1B_LOW();
        MOTOR_PHASE_2A_LOW();
        MOTOR_PHASE_2B_HIGH();
        break;
    case 3:
        MOTOR_PHASE_1A_LOW();
        MOTOR_PHASE_1B_LOW();
        MOTOR_PHASE_2A_LOW();
        MOTOR_PHASE_2B_HIGH();
        break;
    case 4:
        MOTOR_PHASE_1A_LOW();
        MOTOR_PHASE_1B_HIGH();
        MOTOR_PHASE_2A_LOW();
        MOTOR_PHASE_2B_HIGH();
        break;
    case 5:
        MOTOR_PHASE_1A_LOW();
        MOTOR_PHASE_1B_HIGH();
        MOTOR_PHASE_2A_LOW();
        MOTOR_PHASE_2B_LOW();
        break;
    case 6:
        MOTOR_PHASE_1A_LOW();
        MOTOR_PHASE_1B_HIGH();
        MOTOR_PHASE_2A_HIGH();
        MOTOR_PHASE_2B_LOW();
        break;
    case 7:
        MOTOR_PHASE_1A_LOW();
        MOTOR_PHASE_1B_LOW();
        MOTOR_PHASE_2A_HIGH();
        MOTOR_PHASE_2B_LOW();
        break;
    }
}
    PRN_POWER_CHARGE();
    tp.phase += direction;
}

/* 加热时间根据热敏头的温度进行调整*/
static uint32_t tp_heat_dots_adj(uint32_t tm,uint16_t dots)
{
#if (TP_MAX_HEAT_DOT == 64)

    const uint8_t dot_ratio_tbl[TP_MAX_HEAT_DOT/4]=
    {
        80, 75, 70, 70, 73, 74,//0-24
        86, 88, 89, 89, 99, 99,
        110,130,130,130,
    };

#elif (TP_MAX_HEAT_DOT == 96)
    const uint8_t dot_ratio_tbl[TP_MAX_HEAT_DOT/4]=
    {	
        85, 89, 91, 92, 93, 94,//0-24
        97, 100, 103, 106, 109, 113,
	117, 124, 131, 138, 145, 152,
	159, 163, 170, 177, 184, 192,
    };
#elif (TP_MAX_HEAT_DOT == 128)
    const uint8_t dot_ratio_tbl[TP_MAX_HEAT_DOT/4]=
    {
        50, 54, 56, 57, 58, 59,//0-24
        52, 52, 52, 53, 53, 53,
        94, 94, 94, 95, 95, 95,
        96, 96, 96, 98, 98, 98,
        100,100,100,100,100,100,
        100,100,
    };
#else
#error    "NO Define ERROR"
#endif

    if(dots < TP_MAX_HEAT_DOT)
    {
        tm = tm * (dot_ratio_tbl[dots/4]+5)/100;
    }
    else
    {
        tm = tm * (dot_ratio_tbl[TP_MAX_HEAT_DOT/4-1]+34)/100;//+18
    }
    return tm;
}

static uint32_t tp_heat_pre_line_adj(uint32_t tm)
{
    int16_t degree;

    //degree = tp_temp_value();
    degree = tp_states.temperature;//改为前台获取打印机温度
    
    if ((degree < 80) && (degree >= 25)) {
        tm = tm * (Heat_Temperater_Ratio[degree - 25])/1000;
    }
    if (degree < 25) {
        tm = tm * (Heat_TemperaterLow_Ratio[25 - degree])/1000;
    }
    return tm;
}

static void tp_battery_voltage_adj(uint16_t battery_voltage)
{
    if ((battery_voltage <= 410) && (battery_voltage > 350)) {
        tp.heat_adj = tp.heat_setting * Battery_Voltage_Ratio[(410 - battery_voltage)]/100;
    } 
    else if(battery_voltage <= 350)
    {
        tp.heat_adj = tp.heat_setting * Battery_Voltage_Ratio[(410 - 350)]/100;
    }
    else
    {
        tp.heat_adj = tp.heat_setting; 
    }
}

/*计算步进时间以及加速参数*/
static void tp_adjust_steptime(uint8_t heat_cnt,uint16_t max_heat_dots)
{
    uint32_t heat;
    uint32_t time, time_sum;
    uint16_t i;
    uint16_t battery_vol;

//    battery_vol = tp_power_value();
  //  battery_vol =410;
    battery_vol = tp_states.voltage;
    tp_battery_voltage_adj(battery_vol);
    heat = tp.heat_adj;
    heat = tp_heat_pre_line_adj(heat);
    heat = tp_heat_dots_adj(heat, max_heat_dots);

    tp.heat = heat;
    if (heat_cnt == 1) {
        if (tp.last_cnt < 4) {
            tp.last_cnt++;
        } else {
            heat += TpMinWaitTime;
        }
    } else {
        tp.last_cnt = 0;
    }
    
    heat *= heat_cnt;
    heat += TpMinWaitTime;
    //速度限制
    if(heat < tp.dotline_min_time)
    {
        heat = tp.dotline_min_time;
    }

    while(1) {
        time_sum = 0;
        for(i = 0; i < TP_MAX_STROBE_STEP; i++) {
             /* 加速表内继续加速*/
            if ((tp.accel+i) < (ARRAY_SIZE(TpAccelerationSteps) - 1)){
                time = TpAccelerationSteps[tp.accel+i];
            } else {
                /*加速表最后一步*/
                time = TpAccelerationSteps[ARRAY_SIZE(TpAccelerationSteps)-1];
            }
            tp.feed_time[i] = time;
            time_sum += time;
        }
        if (time_sum < heat) {
            /* 原来的变速速程序会直接跳变*/
            if (tp.accel){
                tp.accel--;
                /* bug */
            } else {
                for (i = 0; i < TP_MAX_STROBE_STEP; i++) {
                    tp.feed_time[i] = heat/TP_MAX_STROBE_STEP;      // 加速时间均分
                }
                break;
            }
        } else{
            /* 计算下一次加速参数，增加所打印点行所需要的加速数值*/
            if ((tp.accel+TP_MAX_STROBE_STEP) < (ARRAY_SIZE(TpAccelerationSteps)-1)) {
                tp.accel += TP_MAX_STROBE_STEP;
            } else {
                tp.accel = (ARRAY_SIZE(TpAccelerationSteps)-1);
            }
            break;
        }
    }
}

static uint16_t tp_get_steptime(void)
{
    uint16_t time;

    time = TpAccelerationSteps[tp.accel];
    if (tp.accel < (ARRAY_SIZE(TpAccelerationSteps) - 1)){
        tp.accel++;
    }

    return time;
}

static void tp_set_time_cnt(uint16_t tm)
{
    if (tm < TpMinWaitTime) tm = TpMinWaitTime;
    
//    hal_timer_init(HEAT_TIMER, tm, tp_printer_handler);
    tp_timer_start(TPRINT_TIMER_ID, tm, tp_printer_handler);
}

static uint8_t tp_feed_step(void)
{
    /* set timer */
    tp_set_time_cnt(tp_get_steptime());    
//    if (tp_printer_ready() != TRUE ) {
//        return 0;
//    }

    if (tp.feedmax) {
        tp_forward_step(1);
        tp.feedmax--;
    }

    if (tp.feedmax){
        return 1;
    } else {
        return 0;
    }
}

static void tp_Int_set_pre_idle(void)
{
    /* stop heat */
    STROBE_1_OFF();
    /* 每1ms中断一次 */
    tp.feedmax = 60*1;        
    tp.state = TPSTATE_PRE_IDLE;
}

static void tp_int_set_idle(void)
{
    /* stop heat */
    STROBE_1_OFF();
    /* disable interrupt  */
    DISABLE_TIMER_INTERRUPT();
    MOTOR_PWR_OFF();
    tp.last_cnt = 0;
    tp.state = TPSTATE_IDLE;
}

static uint16_t tp_max_heat_Dots_adj(uint16_t dots)
{
    uint16_t max_heat_dot;

    max_heat_dot = dots/(dots/(tp.heat_dot_max+1)+1);

    if ((max_heat_dot+1) <= tp.heat_dot_max) {
        max_heat_dot += 1;
    } else {
        max_heat_dot = tp.heat_dot_max;
    }

    return max_heat_dot;
}

