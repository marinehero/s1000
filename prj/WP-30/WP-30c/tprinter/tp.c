static uint8_t tp_start(void)
{
    uint8_t i;
    
    //打印机已经在打印，继续打印
    if (tp.state != TPSTATE_IDLE){
        return TP_OK;
    }

    //开始新的一轮打印
    tp_states.paper_out_count = 0;
    tp_states.count =0;
    //电压检测,--------修改电压检测口线PWM延时值---16年11月9日 
    for (i = 0; i < 75; i++) {
        PRN_POWER_DISCHARGE();
        s_DelayUs(100);
        PRN_POWER_CHARGE();
        s_DelayUs(100);
    }
    tp_states.voltage = tp_power_value();
    if(tp_states.voltage > POWRER_LOW_ALARM_LV2){
        //TRACE("\r\ntp dot 96 voltaeg:%d",tp_states.voltage);
        tp.heat_dot_max = TP_MAX_HEAT_DOT; 
    }else if(tp_states.voltage > POWRER_LOW_ALARM_LV1){
        //TRACE("\r\ntp dot 64 voltaeg:%d",tp_states.voltage);
        tp.heat_dot_max = TP_MAX_HEAT_DOT_64; 
    } else if(tp_states.voltage < POWRER_LOW_ALARM_LV1)
    {
        tp.head = 0;
        tp.tail = 0;
        return TP_POWER_OUT_MASK;//电压过低
    }
    
    //温度检测
    tp_states.temperature = tp_temp_value();
    if(tp_states.temperature > TEMP_HIGH_ALARM - 5)
    {
        tp.head = 0;
        tp.tail = 0;

        return TP_TEMP_MASK;
    } 
    tp.state = TPSTATE_START;
  
    tp.accel = 0;
//打印速度限制,速度不能过快 
    tp.dotline_min_time = 0;
    tp_battery_voltage_adj(410);
    tp.dotline_min_time = tp.heat_adj;
    tp.dotline_min_time = tp_heat_pre_line_adj(tp.dotline_min_time);
    tp.dotline_min_time = tp_heat_dots_adj(tp.dotline_min_time, TP_MAX_HEAT_DOT);
    tp.dotline_min_time *= LineDot/TP_MAX_HEAT_DOT/3;
    tp.dotline_min_time += TpMinWaitTime;

    
    tp_timer_start(TPRINT_TIMER_ID, TpAccelerationSteps[0], tp_printer_handler);
    
    return TP_OK;    
}

static uint8_t tp_printcmd_to_buf(uint8_t cmd, uint8_t *dot, uint8_t len)
{
    uint8_t percent;
    uint32_t head;

/***************************************************/
    //温度检测 
    tp_states.temperature = tp_temp_value();
    if((tp_states.temperature > TEMP_HIGH_ALARM) && (tp_states.ignore.temperature == FALSE))
    {
        return TP_TEMP_MASK;
    }
    //缺纸检测 
    //TRACE("\r\npaper:%d",tp_states.paper);
    if(tp_states.ignore.paper_out == FALSE) {
        tp_states.paper = tp_printer_ready();
        if(tp_states.paper == FALSE){
            return TP_PAPER_READY_MASK; 
        }
    }

/******************************/

    head = (tp.head+1) & (ARRAY_SIZE(TP_dot)-1);
    //已用空间，占总空间的百分比 
    percent = (((head + (ARRAY_SIZE(TP_dot)-1) - tp.tail) % (ARRAY_SIZE(TP_dot)-1))*100)/((ARRAY_SIZE(TP_dot)-1));
    while(percent > 75)
    {
        percent = (((head + (ARRAY_SIZE(TP_dot)-1) - tp.tail) % (ARRAY_SIZE(TP_dot)-1))*100)/((ARRAY_SIZE(TP_dot)-1));
        s_DelayUs(500);
    }
    memcpy(TP_dot[tp.head & (ARRAY_SIZE(TP_dot)-1)], dot, len);
    TP_dot[tp.head][LineDot/8] = cmd;
    tp.head = (tp.head+1) & (ARRAY_SIZE(TP_dot)-1);
    
    return tp_start();
    
}

uint8_t tp_print_line(uint8_t *dot)
{
    return tp_printcmd_to_buf(TP_CMD_PRINT_DOTLINE, dot, LineDot/8);
}

uint8_t tp_feed_line(uint16_t line)
{
    line <<= (TP_MAX_STROBE_STEP/2);        // 一个点行等于TP_MAX_STROBE_STEP步
    return tp_printcmd_to_buf(TP_CMD_FEED, (uint8_t *)(&line), sizeof(line));
}

void tp_feed_to_mark(uint16_t line)
{
    line <<= 1;        // 一个点行等于两步
    tp_printcmd_to_buf(TP_CMD_FEED_TO_MARK, (uint8_t *)(&line), sizeof(line));
}

void tp_cut(uint8_t partial)
{
    tp_printcmd_to_buf(TP_CMD_CUT, (uint8_t *)(&partial), sizeof(partial));
}

